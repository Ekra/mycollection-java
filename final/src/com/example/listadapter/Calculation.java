package com.example.listadapter;

public class Calculation {
	
	public static int addition(int numberone,int numbertwo) 
	{
		int result = numberone + numbertwo;
		return result;

	}
	public static int subtraction(int numberone,int numbertwo) 
	{
		int result = numberone - numbertwo;
		return result;

	}

}
